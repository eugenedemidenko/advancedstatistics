if(job==1) #plot all digits from train set
{
	alx=read.csv("c:\\StatBoook\\mnist_train.csv")
	npix=28 # number of pixels in the digit image
	dm=dim(alx) # must be 59999 x (1+28^2)
	par(mfrow=c(2,5),mar=c(1,1,2,1),cex.main=1.5)
	dig=alx[,1]
	for(id in 0:9)
	{
		alxi=alx[dig==id,2:dm[2]]
		n=nrow(alxi)
		alldi=255-colMeans(alxi)
		di <- matrix(alldi,byrow=F,ncol=npix,nrow=npix)
		di<-di[,seq(from=npix,to=1,by=-1)]
		image(1:npix, 1:npix, di, xlab = "", ylab = "",axes = F,col=gray((0:255)/255))
		title(paste("n =",n))
	}	
}
if(job==2)  #plot all digits from test set
{
	alx=read.csv("c:\\StatBoook\\mnist_test.csv")
	npix=28
	dm=dim(alx)
	par(mfrow=c(2,5),mar=c(2,2,2,1),cex.main=1.5)
	dig=alx[,1]
	for(id in 0:9)
	{
		alxi=alx[dig==id,2:dm[2]]
		n=nrow(alxi)
		alldi=255-colMeans(alxi)
		di <- matrix(alldi,byrow=F,ncol=npix,nrow=npix)
		di<-di[,seq(from=npix,to=1,by=-1)]
		image(1:npix, 1:npix, di, xlab = "", ylab = "",axes = F,col=gray((0:255)/255))
		title(paste("n =",n))
	}
}
if(job==3) # computing chi-square criterion and predict the digit from the test set which minimizes the Pearson/Wald test statistic\
# this job may take several hours
{	
	alx=read.csv("C:\\StatBoook\\mnist_train.csv")
	npix=28 # number of pixels in the digit image
	m=27^2 #the number of categories/pixels for each digit
	dm=dim(alx) # must be 59999 x (1+28^2)
	dig=alx[,1] #actual digit
	alldig=matrix(0,nrow=10,ncol=dm[2]-1) #average intensity for each digit
	for(id in 0:9)
	{
		alxi=255-alx[dig==id,2:dm[2]] # reduce the imapct of zero background
		cm=colMeans(alxi)
		alldig[id+1,]=cm/sum(cm) #treat as p0
	}
	#sum of elements in each row of matrix alldig is 1
	
	alx=read.csv("C:\\StatBoook\\mnist_test.csv") #
	dig.true=alx[,1]
	dm=dim(alx)
	achisq=lr=rep(0,10)
	#idmin=matrix(NA,ncol=2,nrow=dm[1])
	idmin=matrix(NA,ncol=3,nrow=dm[1])
	idmin[,1]=dig.true
	#for(j in 1:dm[1])  #loop over digits to recognize	
	for(j in 1:dm[1])  #loop over digits to recognize	
	{
		alxj=255-alx[j,2:dm[2]]
		nj=sum(alxj)
		p.hat=alxj/nj
		for(id in 0:9) #compute the chi-square criterion for each digit
		{
			achisq[id+1]=nj*sum((alldig[id+1,]-p.hat)^2/alldig[id+1,]) #chi-square test
			
			x=p.hat;y=alldig[id+1,]
			i0=(x>0) & (y>0)
			x=x[i0];y=y[i0]
			lr[id+1]=sum(x*log(x/y)) #LR-square test				
		}
		idmin[j,2]=(0:9)[achisq==min(achisq)] #the predicted digit minimizes the chi-square Pearson/Wald test statistic						
		idmin[j,3]=(0:9)[lr==min(lr)] #the predicted digit minimizes the LR test statistic						
	}
	#idmin=dig.mnist(job=3)
	print(date())
	return(idmin) # returns a matrix 10000 x 2. The first column is the true digit and the second column is the predicted digit	
}
if(job==4) #plot the confusion matrix and its image
#the (i,j)th entry is the % of true digit (i-1) predicted as digit (j-1). The sum of elements in each row is 100%
{
	par(mfrow=c(1,1),mar=c(3.5,3.5,1,1))
	confM=matrix(ncol=10,nrow=10)
	for(id.true in 0:9)
	for(id.pred in 0:9)
	confM[id.true+1,id.pred+1]=sum(idmin[,1]==id.true & idmin[,2]==id.pred)/sum(idmin[,1]==id.true)*100
	br=c(.3,.4,.5,.6,.7,.8,.9)
	#image(0:9,0:9,confM,breaks=br,col=gray(1-br[1:(length(br)-1)]))
	image(0:9,0:9,confM,col=gray((255:100)/255),axes=F,xlab="",ylab="")
	mtext(side=1,"True digit",line=2.4,cex=1.4)
	mtext(side=2,"Predicted digit",line=2.25,cex=1.4)
	axis(side=1,0:9,0:9)
	axis(side=2,0:9,0:9)
	for(i in 0:9)
	for(j in 0:9) text(i,j,round(confM[i+1,j+1]),col=2,font=2,cex=1.5)
	return(confM)
}
}
